# 字串分析
words = "she sell sea shell on the sea shore"
print(words)
print("有幾個 sea:", words.count("sea"))  # 有幾個 sea
print("有幾個 s:", words.count("s"))  # 有幾個 s
print("有幾個字元:", len(words))  # 有幾個字元
print("sell的位置:", words.find("sell"))  # sell的位置
print("happy的位置:", words.find("happy"))  # happy的位置

